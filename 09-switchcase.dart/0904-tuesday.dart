void main() {
  int a = 2;
  switch (a = 2) {
    case 1:
      print("monday");

      break;
    case 2:
      print("tuesday");
    case 3:
      print("wednesday");
    case 4:
      print("thrusday");
    case 5:
      print("friday");
    case 6:
      print("saturday");
    case 7:
      print("sunday");

    default:
      print("out of range");
  }
}
